package com.javarush.task.task04.task0438;

/* 
Рисуем линии
*/

public class Solution {
    public static void main(String[] args) throws Exception {

        int n = 10;
        for (int i = 1; i <= n; i++) System.out.print("8");
        System.out.print("\n"); //++++
        for (int j = 1; j <= n; j++) System.out.println("8");
    }
}

